module ALU
#(
    parameter int WIDTH = 32                               // ??????????? ?????? (?????? 32, ?? ? ????????? ????? ???? 8)
)
(
    input  logic                 reset_n,                  // ???????? ?????? reset: 0 = ?????
    input  logic [WIDTH-1:0]     src_a,                    // ??????? A
    input  logic [WIDTH-1:0]     src_b,                    // ??????? B
    input  logic [2:0]           alu_control,              // ??? ???????? ALU

    output logic [WIDTH-1:0]     alu_result,               // ????????? ALU
    output logic                 zero_flag                 // ???? ????: 1 ???? alu_result == 0
