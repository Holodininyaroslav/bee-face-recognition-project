    // ??????? ???????? mem:
    // $readmemh("prog3.txt", dut.im.mem);

    // ???? ?? ????????????? ? ???????? rom:
    // $readmemh("prog3.txt", dut.im.rom);

    // ???? ?? ???? ?? ???????? ? ?????? InstrMem.sv ? ???????? ?????? ??? ???????.
  end

  //======================================================
  // 2) ?????????? ? ???????? ??????????
