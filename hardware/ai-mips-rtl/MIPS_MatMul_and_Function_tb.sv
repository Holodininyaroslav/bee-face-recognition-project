// ???????? ??? ????? ?????????, ??? ???? ? MatrixAccel, ? ReLU4.
//
// ??? ??????:
// 1) ????????? HEX-?????????? ?? ????? (?? ????????? "prog3.txt") ? imem[]
// 2) ???? MIPS instruction = imem[pc>>2]
// 3) ????????? DataMem: read_data = dmem[alu_out>>2], ?????? ?? mem_write
// 4) ???????? ?????? ?????? ? ?????? (?????, ??????, ??????)
// 5) ????? ? ????? ??????? ?????? N ???? dmem
//==============================================================

module MIPS_MatMul_and_Function_tb;

  // ----------------------------
  // ?????????
  // ----------------------------
  parameter string PROG_FILE = "prog3.txt"; // ???? ??????? hex-??????????
  parameter int    IMEM_DEPTH_WORDS = 256;  // ??????? 32-?????? ???? ? instr mem
  parameter int    DMEM_DEPTH_WORDS = 256;  // ??????? 32-?????? ???? ? data mem

  // ----------------------------
  // Clk/Reset
  // ----------------------------
  logic clock;
  logic reset_n;

  // ----------------------------
  // ????????? ? DUT
  // ----------------------------
  logic [31:0] instruction;
  logic [31:0] read_data;

  logic [31:0] pc;
  logic [31:0] alu_out;
  logic [31:0] write_data;
  logic        mem_write;

  // ----------------------------
  // ?????? ??????????/??????
  // ----------------------------
  logic [31:0] imem [0:IMEM_DEPTH_WORDS-1];
  logic [31:0] dmem [0:DMEM_DEPTH_WORDS-1];

  // ============================
  // DUT
  // ============================
  MIPS dut (
    .reset_n     (reset_n),
    .clock       (clock),
    .instruction (instruction),
    .read_data   (read_data),
    .pc          (pc),
    .alu_out     (alu_out),
    .write_data  (write_data),
    .mem_write   (mem_write)
  );

  // ============================
  // Clock
  // ============================
  initial begin
    clock = 1'b0;
    forever #5 clock = ~clock; // 100MHz
  end

  // ============================
  // Init memories
  // ============================
  integer i;
  initial begin
    // ??????? dmem (????? ?? ???? X)
    for (i = 0; i < DMEM_DEPTH_WORDS; i++) dmem[i] = 32'h0000_0000;

    // ???????? imem ?? ?????
    // ????: ?? ????? 32-?????? ?????????? ? HEX ?? ?????? (??? ? ????)
    $display("Loading program from: %s", PROG_FILE);
    $readmemh(PROG_FILE, imem);
  end

  // ============================
  // Instruction fetch (????????????)
  // ============================
  logic [$clog2(IMEM_DEPTH_WORDS)-1:0] imem_idx;
  always_comb begin
    imem_idx = pc[($clog2(IMEM_DEPTH_WORDS)+1):2]; // pc>>2 ?? ??????? imem
    if (pc[31:2] >= IMEM_DEPTH_WORDS)
      instruction = 32'h0000_0000; // NOP ???? ????? ?? ???????
    else
      instruction = imem[imem_idx];
  end

  // ============================
  // Data read (????????????)
  // ???????? ?? ?????: alu_out>>2
  // ============================
  logic [$clog2(DMEM_DEPTH_WORDS)-1:0] dmem_idx;
  always_comb begin
    dmem_idx = alu_out[($clog2(DMEM_DEPTH_WORDS)+1):2];
    if (alu_out[31:2] >= DMEM_DEPTH_WORDS)
      read_data = 32'h0000_0000;
    else
      read_data = dmem[dmem_idx];
  end

  // ============================
  // Data write (?? posedge)
  // ============================
  always_ff @(posedge clock) begin
    if (reset_n && mem_write) begin
      if (alu_out[31:2] < DMEM_DEPTH_WORDS) begin
        dmem[dmem_idx] <= write_data;
        $display("[%0t] SW: dmem[%0d] <= 0x%08h   (addr=0x%08h)",
                 $time, dmem_idx, write_data, alu_out);
      end else begin
        $display("[%0t] SW OUT OF RANGE: addr=0x%08h data=0x%08h",
                 $time, alu_out, write_data);
      end
    end
  end

  // ============================
  // Reset + run
  // ============================
  initial begin
    reset_n = 1'b0;
    repeat (5) @(posedge clock);
    reset_n = 1'b1;

    // ??? ????????? ????? ???????????
    // (???? ? ???? ??????????? ???? ? ?????, ?????? ????? ??????? ?????)
    repeat (400) @(posedge clock);

    // ?????? ?????? ???? dmem (?????? ?????? ????????)
    $display("\n==== DMEM DUMP [0..31] ====");
    for (i = 0; i < 32; i++) begin
      $display("dmem[%0d] = 0x%08h", i, dmem[i]);
    end

    $display("==== END ====\n");
    $stop;
  end

endmodule

`default_nettype wire