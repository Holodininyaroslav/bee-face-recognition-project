  // ---------------- TB signals ----------------
  logic        clock;
  logic        reset_n;
  wire [15:0]  test_value;   // ???? ? Top ??????? "?????" DataMem/??????????

  localparam int CLKPERIOD = 10;      // ns
  localparam int SIM_TIME  = 5_000;   // ns (5 us)

  // ---------------- DUT ----------------
  Top dut (
    .reset_n   (reset_n),
    .clock     (clock),
    .test_value(test_value)
