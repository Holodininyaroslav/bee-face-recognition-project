//-----------------?????????? ???????-----------------\\
logic [2:0]       state;                                   // ????????? ????? ???? ???????? (????? ???? ?? ???????????? alu_control ????????)
logic [WIDTH-1:0] cla_out;                                 // ????? ?? CLA-????????? (?????? WIDTH!)
logic             cla_cout;                                // Carry-out ?? CLA (??????????, ????? ?? ???? ?????? "missing port")

//-----------------???? ????????-----------------\\
localparam logic [2:0]
    OP_AND      = 3'b000,                                  // ????????? AND
    OP_OR       = 3'b001,                                  // ????????? OR
    OP_ADD      = 3'b010,                                  // ???????? (????? CLA)
    OP_NOTUSED1 = 3'b011,                                  // ?? ???????????? (?????? 0)
    OP_NOTUSED2 = 3'b100,                                  // ?? ???????????? (?????? 0)
    OP_MUL      = 3'b101,                                  // ?????????
    OP_SUB      = 3'b110,                                  // ?????????
    OP_SLT      = 3'b111;                                  // Set Less Than (???? A < B ? 1, ????? 0)

//-----------------????????????? ?????? ALU-----------------\\
always_comb begin
    // ???????? ?? ????????? (?????, ????? ?? ???? "???????"/latch)
    alu_result = '0;
    state      = OP_NOTUSED1;

    if (!reset_n) begin
        // ???? reset ??????? ? ?????????? ??? ? 0
        alu_result = '0;
        state      = OP_NOTUSED1;
    end
    else begin
        // Reset ?? ??????? ? ????????? ????????? ????????
        state = alu_control;

        unique case (state)
            OP_AND: alu_result = src_a & src_b;            // AND

            OP_OR : alu_result = src_a | src_b;            // OR

            // ADD ????? ?? CLA-????????? (?? ????????? src_a + src_b)
            OP_ADD: alu_result = cla_out;

            OP_SUB: alu_result = src_a - src_b;            // SUB

            OP_MUL: alu_result = src_a * src_b;            // MUL

            // SLT: ????????? ?????? ???? WIDTH-??????.
            // ???? A < B ? ...0001, ????? ...0000
            OP_SLT: alu_result = (src_a < src_b)
                               ? {{(WIDTH-1){1'b0}}, 1'b1}
                               : '0;

            // ?????????????? ???? ? 0
            OP_NOTUSED1,
            OP_NOTUSED2: alu_result = '0;

            // ????? ?????? ??? ? 0
            default: alu_result = '0;
        endcase
    end
end

