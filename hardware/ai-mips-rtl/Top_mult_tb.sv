  // ---------------- clock ----------------
  initial begin
    clock = 1'b0;
    forever #(CLKPERIOD/2) clock = ~clock;
  end

  // ---------------- reset (active-low) ----------------
  initial begin
    reset_n = 1'b0;
    #50;            // ?????? reset ????????, ????? ??? "????????????"
