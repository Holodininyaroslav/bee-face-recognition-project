`timescale 1ns/1ps
module DFF_ASYNC_tb;

reg  reset_n_tb;
reg  clock_tb;
reg  d_tb;
wire q_tb;

// DUT
DFF_ASYNC dut (
    .reset_n(reset_n_tb),
    .clock  (clock_tb),
    .d      (d_tb),
    .q      (q_tb)
);

// Clock: ?????? 10 ??
initial begin
    clock_tb = 1'b0;
    forever #5 clock_tb = ~clock_tb;
end

// ??????????
initial begin
    $monitor("%0t  reset_n=%b  clk=%b  d=%b  -> q=%b",
             $time, reset_n_tb, clock_tb, d_tb, q_tb);
end

// ????
initial begin
    // ????????? ????????
    reset_n_tb = 1'b1;
    d_tb       = 1'b0;

    // 1) ??????????? reset
    #2  reset_n_tb = 1'b0;   // ?????
    #8  reset_n_tb = 1'b1;   // ????????? reset

    // 2) ???????? ??????? ?????? ?? posedge
    // q ?????? ???????? ?????? ?? posedge clock
    #3  d_tb = 1'b1;         // ?????? d ????? ???????
    #20 d_tb = 1'b0;         // ??? ??? ??????
    #20 d_tb = 1'b1;

    // 3) reset ?????????? ?????? (?? ?? ??????)
    #7  reset_n_tb = 1'b0;
    #4  reset_n_tb = 1'b1;

    #30 $finish;
end

endmodule
