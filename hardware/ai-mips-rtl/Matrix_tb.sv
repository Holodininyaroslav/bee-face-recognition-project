module Mat_mult_tb;

  // DUT ports
  logic [31:0] A;
  logic [31:0] B;
  logic [31:0] Res;

  // ??????? DUT
  Mat_mult dut (
    .A   (A),
    .B   (B),
    .Res (Res)
  );

  // --------- helpers ----------
  function automatic [31:0] pack2x2(
    input logic [7:0] a00, input logic [7:0] a01,
    input logic [7:0] a10, input logic [7:0] a11
  );
    pack2x2 = {a00, a01, a10, a11};
  endfunction

  function automatic [7:0] mmul_elem(
    input logic [7:0] x0, input logic [7:0] x1,
    input logic [7:0] y0, input logic [7:0] y1
  );
    // (x0*y0 + x1*y1) -> ????? ??????? 8 ??? (??? ??????? ?????? ? 8-?????? ????????)
    logic [17:0] sum;
    begin
      sum = (x0 * y0) + (x1 * y1);
      mmul_elem = sum[7:0];
    end
  endfunction

  function automatic [31:0] expected_res(input logic [31:0] Ain, input logic [31:0] Bin);
    logic [7:0] a00,a01,a10,a11;
    logic [7:0] b00,b01,b10,b11;
    logic [7:0] r00,r01,r10,r11;
    begin
      {a00,a01,a10,a11} = Ain;
      {b00,b01,b10,b11} = Bin;

      // C = A×B:
      // r00 = a00*b00 + a01*b10
      // r01 = a00*b01 + a01*b11
      // r10 = a10*b00 + a11*b10
      // r11 = a10*b01 + a11*b11
      r00 = mmul_elem(a00,a01, b00,b10);
      r01 = mmul_elem(a00,a01, b01,b11);
      r10 = mmul_elem(a10,a11, b00,b10);
      r11 = mmul_elem(a10,a11, b01,b11);

      expected_res = {r00,r01,r10,r11};
    end
  endfunction

  task automatic run_case(
    input logic [7:0] a00, input logic [7:0] a01,
    input logic [7:0] a10, input logic [7:0] a11,
    input logic [7:0] b00, input logic [7:0] b01,
    input logic [7:0] b10, input logic [7:0] b11,
    input string      name
  );
    logic [31:0] exp;
    begin
      A = pack2x2(a00,a01,a10,a11);
      B = pack2x2(b00,b01,b10,b11);

      // ??????. ?????? ????? ???? ?? 1 ??????-????
      #1;

      exp = expected_res(A,B);

      $display("--------------------------------------------------");
      $display("CASE: %s", name);
      $display("A = %h  (a00=%0d a01=%0d a10=%0d a11=%0d)", A, a00,a01,a10,a11);
      $display("B = %h  (b00=%0d b01=%0d b10=%0d b11=%0d)", B, b00,b01,b10,b11);
      $display("RES = %h, EXP = %h", Res, exp);

      if (Res === exp) $display("PASS");
      else begin
        $display("FAIL");
        $fatal(1);
      end
    end
  endtask

  // --------- main ----------
  initial begin
    // ????? ? wave ?? ???? X
    A = '0;
    B = '0;
    #1;

    // 1) ??????? ???? (????????? ?????)
    // A = [ [1,2],[3,4] ], B = [ [5,6],[7,8] ]
    run_case(8'd1,8'd2,8'd3,8'd4,
             8'd5,8'd6,8'd7,8'd8,
             "small_numbers");

    // 2) ????????? ??????? * ???-?? = ???-??
    // I * B = B
    run_case(8'd1,8'd0,8'd0,8'd1,
             8'd9,8'd10,8'd11,8'd12,
             "identity_times_B");

    // 3) ???-?? * I = ???-??
    run_case(8'd13,8'd14,8'd15,8'd16,
             8'd1,8'd0,8'd0,8'd1,
             "A_times_identity");

    // 4) ???????? ???????????? (?????????? ??????? ?????)
    // ??? ????????? ???? ?? ??????? 8 ?????
    run_case(8'd200,8'd200,8'd200,8'd200,
             8'd200,8'd200,8'd200,8'd200,
             "overflow_low8");

    $display("==================================================");
    $display("ALL TESTS PASSED");
    $display("==================================================");
    #10;
    $stop;
  end

endmodule

